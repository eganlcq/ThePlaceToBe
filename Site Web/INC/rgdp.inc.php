    <ul>
        <li><h3>Données personnelles recueillies lorsque vous vous inscrivez :</h3>
            <p>Lorsque vous vous inscrivez, nous collectons certaines données personnelles pour vous permettre d'utiliser les fonctionnalités du site web et de l'application.</p><br>
        </li>
        <li>
            <h3>Données personnelles recueillies via votre utilisation de nos produits :</h3>
            <p>lorsque vous utilisez nos services, nous collectons des données personnelles relatives à votre utilisation, comme vos favoris/achievements que vous avez rajouter au fil du temps.</p><br>
        </li>
    </ul>

    <h1>Comment avoir accès à ces données personnelles</h1>
    <p>Vous pouvez accéder à tout moment à vos données personnelles en regardant dans l'onglet gestion de profil.</p>
    <p>Nous vous laissons les modifier quand bon vous semble.</p><br>

    <h1>Partage/transfert des données personnelles</h1>
    <p>Vu que nous sommes, pour le moment, qu'une toute petite structure, nous ne partageons ni ne transférons vos données personnelles</p>

